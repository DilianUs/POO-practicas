    private void btn_igualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_igualActionPerformed
        double segundoNumero;
        
        if(operacion.equals("nula")){
            txtOperacion.setText("cadenaNumeros");
        }
        
        else if(operacion.equals("suma")){
            segundoNumero = Double.parseDouble(cadenaNumeros);
            resultado = primerNumero + segundoNumero;
            
            txtOperacion.setText(String.format("% .2f", resultado));
            cadenaNumeros = String.valueOf(resultado);//valueOf convierte el double a string
            operacion = "nula";
        }
        else if(operacion.equals("restar")){
            segundoNumero = Double.parseDouble(cadenaNumeros);
            resultado = primerNumero - segundoNumero;
            
            txtOperacion.setText(String.format("% .2f", resultado));
            cadenaNumeros = String.valueOf(resultado);//valueOf convierte el double a string
            operacion = "nula";
        }
        else if(operacion.equals("multiplicar")){
            segundoNumero = Double.parseDouble(cadenaNumeros);
            resultado = primerNumero * segundoNumero;
            
            txtOperacion.setText(String.format("% .2f", resultado));
            cadenaNumeros = String.valueOf(resultado);//valueOf convierte el double a string
            operacion = "nula";
        }
        else if(operacion.equals("dividir")){
            segundoNumero = Double.parseDouble(cadenaNumeros);
            
            if(segundoNumero == 0){
               txtOperacion.setText("No se divide por 0");
            }else{
                resultado = primerNumero / segundoNumero;
            
            txtOperacion.setText(String.format("% .2f", resultado));
            cadenaNumeros = String.valueOf(resultado);//valueOf convierte el double a string
            operacion = "nula";
            }
            
        }
        
        txtResultado.setText("");
        activado = true;
    }//GEN-LAST:event_btn_igualActionPerformed

