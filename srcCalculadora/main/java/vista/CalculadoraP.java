    private void bnt_CEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnt_CEActionPerformed
        int tamaño = cadenaNumeros.length();
        if(tamaño > 0){
           if(tamaño == 1){
             cadenaNumeros = "0";
           }else{
          cadenaNumeros = cadenaNumeros.substring(0,cadenaNumeros.length() - 1);
        }
        txtOperacion.setText(cadenaNumeros);
        }
    }//GEN-LAST:event_bnt_CEActionPerformed
 
    private void btn_puntoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_puntoActionPerformed
        if (punto == true){
            if(cadenaNumeros.equals("")){
                cadenaNumeros = "0.";
            }else{
               cadenaNumeros += ".";
            }
            txtOperacion.setText(cadenaNumeros);
            punto = false; 
        } 
        
    }//GEN-LAST:event_btn_puntoActionPerformed

    private void btn_sqrActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_sqrActionPerformed
        primerNumero = Double.parseDouble(cadenaNumeros); 
        txtResultado.setText("sqrt("+cadenaNumeros+")");
        resultado = Math.sqrt(primerNumero);
        txtOperacion.setText(String.format("%.2f", resultado));
        cadenaNumeros = String.valueOf(resultado);
        operacion = "raiz";
        punto = true;
        
    }//GEN-LAST:event_btn_sqrActionPerformed

    private void btn_divisionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_divisionActionPerformed
      if(activado == true){
          primerNumero = Double.parseDouble(cadenaNumeros);
          txtResultado.setText(cadenaNumeros + " / ");
          cadenaNumeros = "";
          operacion = "dividir";
          
          activado = false;
          punto = true;
        }        
    }//GEN-LAST:event_btn_divisionActionPerformed

